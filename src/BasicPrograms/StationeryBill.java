package BasicPrograms;

public class StationeryBill {
	public static void main(String args[]) {
		int eraser=15;
		int pencil=75;
		int scale=30;
		int pen=50;
		int notebook=160;
		int sum=eraser+pencil+scale+pen+notebook;
		System.out.println("eraser="+eraser+"x3="+eraser*3);
		System.out.println("pencil="+pencil+"x5="+pencil*5);
		System.out.println("scale="+scale+"x2="+scale*2);
		System.out.println("pen="+pen+"x5="+pen*5);
		System.out.println("notebook="+notebook+"x4="+notebook*4);
		System.out.println("Total cost="+sum);
	}

}
